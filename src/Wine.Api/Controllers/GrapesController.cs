    /// <remarks>
    /// Fetch a list of 100 wine grapes.
    /// When color is informed, only grapes of given color is returned. Otherwise
    /// no filter will be applied.
    /// The result doesnt obey any sorting, so results may vary.
    /// </remarks>
    /// <param name="color">Optional. The skin color of wanted grapes.</param>
    /// <returns></returns>
    [HttpGet(Name = "ListGrapes")]
    [MapToApiVersion("1.0")]
    public IActionResult List(GrapeColor? color)
    {
        var grapes = _grapes;
        if (color != null) grapes = grapes.Where(w => w.Color == color);
        return Ok(grapes.Take(100));
    }

    /// <summary>
    /// Fetch a list of wine grapes.
    /// </summary>
    /// <remarks>
    /// Fetch a list of wine grapes based on query.
    /// Grape's primeName can contain only the begginng of the name.
    /// If no `skip` is given, 0 is used as default.
    /// If no `take` is given, 10 is used as default.
    /// Sort data should contain the name of the sorting field. If none is set,
    /// sorting will be applied to primeName
    /// </remarks>
    /// <param name="primeName">Optional. The (initial part of) primeName of the grape.</param>
    /// <param name="color">Optional. The skin color of wanted grapes.</param>
    /// <param name="_skip">How many record to skip. Only >= 0 is allowed. Defaults to 0.</param>
    /// <param name="_take">How many record to take. Only > 0 is allowed. Detaults to 10. Limited to 100. </param>
    /// <param name="_sort"></param>
    /// <returns></returns>
    [HttpGet(Name = "ListGrapes")]
    [MapToApiVersion("2.0")]
    [ProducesResponseType((int)HttpStatusCode.OK, Type = typeof(IEnumerable<Grape>))]
    [ProducesResponseType((int)HttpStatusCode.BadRequest)]
    public IActionResult ListV2(string? primeName, GrapeColor? color, uint? _skip, ushort? _take, string? _sort)
    {
        var take = (int)_take.GetValueOrDefault(10);
        if (take < 1) return BadRequest();
        if (take > 100) return BadRequest();

        var grapes = _grapes;
        if (!string.IsNullOrWhiteSpace(primeName))
        {
            grapes = grapes
                .Where(w => w.PrimeName.StartsWith(
                    primeName,
                    StringComparison.InvariantCultureIgnoreCase)
                );
        }
        if (color != null) grapes = grapes.Where(w => w.Color == color);
        if (!string.IsNullOrEmpty(_sort))
        {
            switch (_sort.ToLowerInvariant())
            {
                case "color":
                    grapes = grapes
                        .OrderBy(o => o.Color)
                        .ThenBy(o => o.PrimeName);
                    break;
            }
        } else
        {
            grapes = grapes.OrderBy(o => o.PrimeName);
        }
        Response.Headers.Add("x-total-record", grapes.Count().ToString());
        return Ok(grapes
            .Skip((int)_skip.GetValueOrDefault())
            .Take(take));
    }

    /// <summary>
    /// Get the specified grap base on its primeName
    /// </summary>
    /// <param name="primeName">The primeName of the wanted grape.</param>
    /// <returns></returns>
    [HttpGet("{primeName}", Name = "GetGrape")]
    [MapToApiVersion("1.0")]
    [MapToApiVersion("2.0")]
    [ProducesResponseType((int)HttpStatusCode.OK, Type = typeof(Grape))]
    [ProducesResponseType((int)HttpStatusCode.NotFound)]
    public IActionResult Get(string primeName)
    {
        var grape = _grapes
            .Where(w => w.PrimeName.Equals(primeName, StringComparison.InvariantCultureIgnoreCase));
        if (grape.Count() < 1) return NotFound();
        return Ok(grape.FirstOrDefault());
    }
